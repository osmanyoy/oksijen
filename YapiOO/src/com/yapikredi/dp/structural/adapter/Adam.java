package com.yapikredi.dp.structural.adapter;

public class Adam {
	private String isim;
	private String soyisim;
	private long tel;

	public String getIsim() {
		return this.isim;
	}

	public void setIsim(final String isim) {
		this.isim = isim;
	}

	public String getSoyisim() {
		return this.soyisim;
	}

	public void setSoyisim(final String soyisim) {
		this.soyisim = soyisim;
	}

	public long getTel() {
		return this.tel;
	}

	public void setTel(final long tel) {
		this.tel = tel;
	}

}
